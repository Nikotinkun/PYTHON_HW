#Насчитаем переменные, соответствующие часам, минутам и секундам:
hours = int(user_time) // 3600
minutes = (int(user_time) % 3600) // 60
seconds = (int(user_time) % 3600) % 60

#Выведем время в необходимом формате:
print('Итого времени в часах, минутах и секундах: {}:{}:{}'.format(str(hours).zfill(2), str(minutes).zfill(2), str(seconds).zfill(2)))

