income = int(input('Введите значение выручки фирмы: '))
expense = int(input('Введите значение издержек фирмы: '))

if income > expense:
    print('Фирма отработала с прибылью')
    rent = round(income / expense * 100, 2)
    print('Рентабельность выручки составила {}%'.format(rent))
    emp_num = int(input('Введите численность сотрудников: '))
    income_for_emp = round(income / emp_num, 2)
    print('Прибыль на одного сотрудника составила {} рублей'.format(income_for_emp))
else:
    print('Фирма отработала с убытками')

